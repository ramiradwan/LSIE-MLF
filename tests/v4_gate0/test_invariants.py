def _canonical_utc_timestamp(value: str) -> str:
    """Re-implement the orchestrator's canonical UTC formatter for parity."""
    parsed = datetime.fromisoformat(value.replace("Z", "+00:00"))
    return parsed.astimezone(UTC).isoformat().replace("+00:00", "Z")


def _load_fixtures() -> list[_Fixture]:
    fixtures: list[_Fixture] = []
    for path in sorted(GATE0_FIXTURE_DIR.glob("segment_*.json")):
        fixtures.append(json.loads(path.read_text()))
    return fixtures


def _fixture_ids() -> list[str]:
    paths = sorted(GATE0_FIXTURE_DIR.glob("segment_*.json"))
    return [p.stem.removeprefix("segment_") for p in paths]


@pytest.mark.parametrize("fixture", _load_fixtures(), ids=_fixture_ids())
def test_segment_id_derives_from_stable_identity(fixture: _Fixture) -> None:
    """SHA256(uuid|start|end) — drift here breaks Module C ↔ Module D handoff."""
    stable_identity = "|".join(
        (
            f"{uuid.UUID(fixture['session_id'])}",
            _canonical_utc_timestamp(fixture["segment_window_start_utc"]),
            _canonical_utc_timestamp(fixture["segment_window_end_utc"]),
        )
    )
    expected = hashlib.sha256(stable_identity.encode("utf-8")).hexdigest()
    assert fixture["segment_id"] == expected


@pytest.mark.parametrize("fixture", _load_fixtures(), ids=_fixture_ids())
def test_gate0_fixture_validates_against_handoff_schema(fixture: _Fixture) -> None:
    handoff_data = dict(fixture)
    handoff_data.pop("_experiment_code", None)
    payload = InferenceHandoffPayload.model_validate(handoff_data)
    snapshot = payload.bandit_decision_snapshot

    assert payload.stimulus_modality == fixture["_stimulus_modality"]
    assert payload.stimulus_payload.model_dump() == fixture["_stimulus_payload"]
    assert payload.expected_stimulus_rule == fixture["_expected_stimulus_rule"]
    assert payload.expected_response_rule == fixture["_expected_response_rule"]
    assert "_expected_greeting" not in fixture
    assert "expected_greeting" not in fixture["_bandit_decision_snapshot"]
    assert snapshot.decision_context_hash
    assert isinstance(snapshot.random_seed, int)
    assert snapshot.expected_stimulus_rule
    assert snapshot.expected_response_rule
    assert snapshot.stimulus_payload.text


@pytest.mark.parametrize("fixture", _load_fixtures(), ids=_fixture_ids())
def test_reward_math_replays_to_locked_baseline(fixture: _Fixture) -> None:
    """§7B reward path — drift here invalidates every prior posterior update."""
    series = [
        TimestampedAU12(timestamp_s=obs["timestamp_s"], intensity=obs["intensity"])
        for obs in fixture["_au12_series"]
    ]
    result = compute_reward(
        au12_series=series,
        stimulus_time_s=fixture["_stimulus_time"],
        is_match=True,
    )

    expected = EXPECTED_REWARDS[fixture["segment_id"]]
    assert result.semantic_gate == 1
    assert result.n_frames_in_window == expected["n_frames_in_window"]
    assert result.p90_intensity == pytest.approx(expected["p90_intensity"], abs=1e-10)
    assert result.gated_reward == pytest.approx(expected["gated_reward"], abs=1e-10)


