def test_fractional_beta_bernoulli_update() -> None:
    """α' = α + r, β' = β + (1 - r) — pure math contract, no store required."""
    alpha, beta = 1.0, 1.0
    reward = 0.8166551813
    new_alpha = alpha + reward
    new_beta = beta + (1.0 - reward)

    assert new_alpha == pytest.approx(1.8166551813, abs=1e-12)
    assert new_beta == pytest.approx(1.1833448187, abs=1e-12)
    # Posterior mass is conserved: α' + β' = α + β + 1 for any reward in [0,1].
    assert (new_alpha + new_beta) == pytest.approx(alpha + beta + 1.0, abs=1e-12)


@pytest.mark.parametrize("reward", [0.0, 1.0])
def test_fractional_update_at_bounds(reward: float) -> None:
    """At r=0 only β advances; at r=1 only α advances."""
    alpha, beta = 1.0, 1.0
    new_alpha = alpha + reward
    new_beta = beta + (1.0 - reward)
    if reward == 0.0:
        assert new_alpha == 1.0
        assert new_beta == 2.0
    else:
        assert new_alpha == 2.0
        assert new_beta == 1.0
